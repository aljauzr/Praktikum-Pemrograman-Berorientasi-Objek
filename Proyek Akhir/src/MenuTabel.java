    public void updateData(){
        int index = tblData.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tblData.getModel();
        
        String nim = model.getValueAt(index, 0).toString();
        String nama = model.getValueAt(index, 1).toString();
        String fakultas = model.getValueAt(index, 2).toString();
        String prodi = model.getValueAt(index, 3).toString();
        String email = model.getValueAt(index, 4).toString();
        String tahun = model.getValueAt(index, 5).toString();
        String status = model.getValueAt(index, 6).toString();
        
        viewUpdate.setVisible(true);
        viewUpdate.pack();
        viewUpdate.setLocationRelativeTo(null);
        viewUpdate.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        viewUpdate.lNim.setText(nim); viewUpdate.lNim.setVisible(false);
        viewUpdate.tfNim.setText(nim);
        viewUpdate.lNama.setText(nama); viewUpdate.lNama.setVisible(false);
        viewUpdate.tfNama.setText(nama);
        viewUpdate.lFakultas.setText(fakultas); viewUpdate.lFakultas.setVisible(false);
        viewUpdate.tfFakultas.setText(fakultas);
        viewUpdate.lProdi.setText(prodi); viewUpdate.lProdi.setVisible(false);
        viewUpdate.tfProdi.setText(prodi);
        viewUpdate.lEmail.setText(email); viewUpdate.lEmail.setVisible(false);
        viewUpdate.tfEmail.setText(email);
        viewUpdate.lTahun.setText(tahun); viewUpdate.lTahun.setVisible(false);
        viewUpdate.tfTahun.setText(tahun);
        viewUpdate.lStatus.setText(status); viewUpdate.lStatus.setVisible(false);
        viewUpdate.tfStatus.setText(status);
    } 
    
