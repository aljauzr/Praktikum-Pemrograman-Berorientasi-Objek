    public void tampilData(){
        try {
            DefaultTableModel tblModel = (DefaultTableModel)tblData.getModel();
            tblModel.setRowCount(0);
            
            String query = "SELECT * FROM datamhs";
            connector.statement = connector.koneksi.createStatement();
            ResultSet rs = connector.statement.executeQuery(query); 
            while(rs.next()){
                String nim = rs.getString("nim");
                String nama = rs.getString("nama");
                String fakultas = rs.getString("fakultas");
                String prodi = rs.getString("prodi");
                String email = rs.getString("email");
                String tahun = rs.getString("tahun_masuk");
                String status = rs.getString("status");
                
                String tbData[] = {nim,nama,fakultas,prodi,email,tahun,status};
                
                tblModel.addRow(tbData);
            }
        } catch (Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
